// Get current language from localStorage or default to Spanish
let currentLang = localStorage.getItem('netflix_lang') || 'es';

// Function to get translation
function t(key) {
    return translations[currentLang][key] || key;
}

// Function to change language
function changeLang(lang) {
    if (currentLang === lang) return; // No change needed
    
    currentLang = lang;
    localStorage.setItem('netflix_lang', lang);
    
    // Reload page to apply translations
    window.location.reload();
}

// Function to update all page translations
function updatePageTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = t(key);
        
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = translation;
        } else if (element.hasAttribute('data-i18n-html')) {
            element.innerHTML = translation;
        } else {
            element.textContent = translation;
        }
    });
}

// Initialize translations on page load
if (typeof document !== 'undefined') {
    // Apply translations as soon as possible
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initTranslations);
    } else {
        initTranslations();
    }
}

function initTranslations() {
    // Set up language selectors
    document.querySelectorAll('.language-selector select').forEach(select => {
        select.value = currentLang;
        select.addEventListener('change', function() {
            changeLang(this.value);
        });
    });
    
    // Initial translation
    updatePageTranslations();
}